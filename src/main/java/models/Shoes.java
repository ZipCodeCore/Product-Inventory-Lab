package models;

public class Shoes {

        private  String name;
        private int size;
        private float price;

        public void setName(String name) {
                this.name = name;
        }

        public String getName() {
                return name;
        }

        public void setSize(int size) {
                this.size = size;
        }

        public int getSize() {
                return size;
        }

        public void setPrice(float price) {
                this.price = price;
        }

        public float getPrice() {
                return price;
        }
}



